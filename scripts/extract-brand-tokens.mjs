import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";

const HOMEPAGE_URL = "https://somedevsdo.com";

async function fetchText(url) {
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Request failed for ${url} (${response.status})`);
  }

  return response.text();
}

function findStylesheetUrls(html, baseUrl) {
  const linkPattern = /<link[^>]*rel=["']stylesheet["'][^>]*>/gi;
  const hrefPattern = /href=["']([^"']+\.css[^"']*)["']/i;
  const urls = new Set();

  for (const tag of html.match(linkPattern) ?? []) {
    const hrefMatch = tag.match(hrefPattern);
    if (!hrefMatch?.[1]) continue;
    const href = hrefMatch[1];
    if (!href.includes("/_astro/")) continue;
    urls.add(new URL(href, baseUrl).href);
  }

  return [...urls];
}

function findBlock(css, selector) {
  const start = css.indexOf(selector);
  if (start === -1) return null;

  const open = css.indexOf("{", start + selector.length);
  if (open === -1) return null;

  let depth = 0;
  for (let i = open; i < css.length; i += 1) {
    if (css[i] === "{") depth += 1;
    if (css[i] === "}") {
      depth -= 1;
      if (depth === 0) {
        return css.slice(open + 1, i);
      }
    }
  }

  return null;
}

function extractCustomProperties(blockContent) {
  const tokenPattern = /--([a-z0-9-]+)\s*:\s*([^;]+);/gi;
  const tokens = {};
  let match;

  while ((match = tokenPattern.exec(blockContent)) !== null) {
    const [, key, value] = match;
    tokens[key] = value.trim();
  }

  return tokens;
}

function toKebab(value) {
  return value
    .replace(/[^a-z0-9-]/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "");
}

function isColorToken(name) {
  return (
    name.startsWith("clr-") ||
    ["background", "foreground", "shadow", "secondary-text", "cod-gray", "mine-shaft", "dove-gray", "alto", "white"].includes(name)
  );
}

function isSpacingToken(name) {
  return name.startsWith("size-");
}

function isFontFamilyToken(name) {
  return name.startsWith("font-family-");
}

function toCssVariables(tokens) {
  return Object.entries(tokens)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([name, value]) => `  --${name}: ${value};`)
    .join("\n");
}

function toThemeVariables(tokens) {
  const rows = [];

  for (const name of Object.keys(tokens).sort()) {
    if (isColorToken(name)) {
      rows.push(`  --color-${toKebab(name)}: var(--${name});`);
      continue;
    }
    if (isSpacingToken(name)) {
      rows.push(`  --spacing-${toKebab(name)}: var(--${name});`);
      continue;
    }
    if (isFontFamilyToken(name)) {
      rows.push(`  --font-${toKebab(name)}: var(--${name});`);
    }
  }

  return rows.join("\n");
}

function createBaseCss(lightTokens, darkTokens) {
  const lightTokenCss = toCssVariables(lightTokens);
  const darkTokenCss = toCssVariables(darkTokens);
  const themeTokenCss = toThemeVariables(lightTokens);

  return `/* This file is auto-generated by scripts/extract-brand-tokens.mjs */\n:root {\n${lightTokenCss}\n}\n\n[data-theme="dark"] {\n${darkTokenCss}\n}\n\n@theme inline {\n${themeTokenCss}\n}\n`;
}

function toDarkOverrides(lightTokens, darkTokens) {
  const overrides = {};
  for (const [name, value] of Object.entries(darkTokens)) {
    if (lightTokens[name] !== value) {
      overrides[name] = value;
    }
  }
  return overrides;
}

async function main() {
  const homeHtml = await fetchText(HOMEPAGE_URL);
  const cssUrls = findStylesheetUrls(homeHtml, HOMEPAGE_URL);
  if (cssUrls.length === 0) {
    throw new Error("Could not find any Astro stylesheet URLs on the homepage.");
  }

  const stylesheets = await Promise.all(cssUrls.map((url) => fetchText(url)));
  const css = stylesheets.join("\n");

  const rootBlock = findBlock(css, ":root");
  const darkBlock = findBlock(css, "[data-theme=dark]") ?? findBlock(css, "[data-theme=\"dark\"]");

  if (!rootBlock) {
    throw new Error("Could not find :root custom properties in source CSS.");
  }
  if (!darkBlock) {
    throw new Error("Could not find [data-theme=dark] custom properties in source CSS.");
  }

  const lightTokens = extractCustomProperties(rootBlock);
  const darkTokens = extractCustomProperties(darkBlock);
  const darkOverrides = toDarkOverrides(lightTokens, darkTokens);

  const outputCss = createBaseCss(lightTokens, darkOverrides);
  const outputJson = {
    source: HOMEPAGE_URL,
    stylesheets: cssUrls,
    light: lightTokens,
    dark: darkOverrides
  };

  await mkdir(path.join(process.cwd(), "scripts"), { recursive: true });
  await writeFile(path.join(process.cwd(), "base.css"), outputCss, "utf8");
  await writeFile(path.join(process.cwd(), "tokens.json"), `${JSON.stringify(outputJson, null, 2)}\n`, "utf8");
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
